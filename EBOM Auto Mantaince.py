def connect_to_sap():
    """Connects to active SAP session or initializes a new one."""
    try:
        SapGuiAuto = win32com.client.GetObject("SAPGUI")
        application = SapGuiAuto.GetScriptingEngine
        connection = application.OpenConnection(SAP_SYSTEM_ID, True)
        return connection.Children(0)
    except Exception:
        try:
            subprocess.Popen(SAP_LOGON_PATH)
            time.sleep(10)
            SapGuiAuto = win32com.client.GetObject("SAPGUI")
            return SapGuiAuto.GetScriptingEngine.OpenConnection(SAP_SYSTEM_ID, True).Children(0)
        except Exception as e:
            print(f"Critical: SAP Connection Failed: {e}")
            return None

