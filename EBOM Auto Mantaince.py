def get_sap_dataframe(txt_path):
    """Parses SAP exported TXT files into a clean Pandas DataFrame."""
    try:
        # Wait for file to be generated by SAP
        for _ in range(10):
            if os.path.exists(txt_path) and os.path.getsize(txt_path) > 0:
                break
            time.sleep(1)
        
        df = pd.read_csv(txt_path, sep='\t', skiprows=3, dtype=str, engine='python')
        df = df.dropna(axis=1, how='all')
        df = df.apply(lambda x: x.str.strip() if hasattr(x, "str") else x)
        return df
    except Exception as e:
        print(f"Error parsing SAP export: {e}")
        return pd.DataFrame()

def import_data(file_path):
    """Imports staging data from Excel or CSV."""
    if not os.path.exists(file_path):
        print(f"Error: Source file not found at {file_path}")
        return None
    try:
        if file_path.lower().endswith('.csv'):
            df = pd.read_csv(file_path, header=0)
        else:
            df = pd.read_excel(file_path, header=0, engine='openpyxl')
        return df.fillna('').astype(str)
    except Exception as e:
        print(f"Data Import Error: {e}")
        return None

